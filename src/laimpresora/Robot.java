    private Stack<String> taskStack = new Stack<>();
    private boolean isProcessing = false;
    private Timer progressBarTimer; 
    private final int taskDuration = 3000; 
    public Robot() {
        initComponents();
        progressBarTimer = new Timer(100, new ActionListener() {
            public void actionPerformed(ActionEvent evt) {
                barra();
            }
        });
        progressBarTimer.setRepeats(true);
    }
 private void barra() {
 if (isProcessing) {
        long currentTime = System.currentTimeMillis();
        long elapsedTime = currentTime - startTime;
        int progress = (int) ((double) elapsedTime / taskDuration * 100);
        progress = Math.min(progress, 100);
        jProgressBar1.setValue(progress);

        if (progress >= 100) {
            isProcessing = false;
            jLabel1.setIcon(null); 
            if (!taskStack.isEmpty()) {
                sigta(); // Procesar la pr√≥xima tarea si hay alguna en la pila
            }
        }
    }
    }
private void sigta() {
if (!isProcessing && !taskStack.isEmpty()) {
        String nextTask = taskStack.pop();
        if (nextTask.equals("Sensores")) {
            ejecutar(robot1Icon);
        } else if (nextTask.equals("Comunicadores")) {
           ejecutar(robot2Icon);
        }
    }
}
 private void ejecutar(ImageIcon icon) {
        jLabel1.setIcon(icon);
        isProcessing = true;
        startTime = System.currentTimeMillis(); 
        progressBarTimer.start();
    }

